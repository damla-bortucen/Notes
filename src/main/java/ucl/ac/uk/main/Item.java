package ucl.ac.uk.main;

public abstract class Item {
    protected final String id;
    protected String name;

    public Item(String id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public String getId()
    {
        return id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}

